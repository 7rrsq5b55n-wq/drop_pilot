/// EcoRoute Driver ("the App") helps delivery drivers plan and navigate multi-stop delivery rounds.
///
/// ## Information we collect
/// The App does not require you to create an account.
///
/// The App may process the following information to provide its core features:
/// - **Location (When In Use)**: used to start routes from your current location and show your position on the map.
/// - **Delivery stop addresses**: addresses you enter or import are used to display stops and plan routes.
///
/// ## How information is used
/// - To geocode addresses into map coordinates.
/// - To calculate routes and ETAs, including traffic-aware ETAs where supported.
/// - To allow you to open turn-by-turn navigation in Apple Maps or Google Maps.
///
/// ## Data sharing
/// - When you request geocoding or routing, addresses and/or coordinates may be sent to mapping providers (e.g., Google Maps Platform APIs) to generate results.
/// - We do not sell your personal data.
///
/// ## Data storage
/// - Your rounds and stops are stored locally on your device.
/// - A local cache may store geocoding results to improve speed and reduce repeated requests.
///
/// ## Your choices
/// - You can delete rounds/stops within the App.
/// - You can deny location permission, but route-start features may not work.
